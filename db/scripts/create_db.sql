   POSTDATE      TIMESTAMP,
   CLEARDATE     TIMESTAMP,
   DOCDESC       VARCHAR(48),
   XACTREF       VARCHAR(32),
   CUSTREF       VARCHAR(32),
   EXTREF5       VARCHAR(32),
   DOCCURR       VARCHAR(3),
   HOMECURR      VARCHAR(3),
   ALTCURR       VARCHAR(3),
   ORGVAL_DOC    DOUBLE,
   ORGVAL_HOME   DOUBLE,
   ORGVAL_ALT    DOUBLE,
   ORGVAL_NORM   DOUBLE,
   OPENVAL_DOC   DOUBLE,
   OPENVAL_HOME  DOUBLE,
   OPENVAL_ALT   DOUBLE,
   OPENVAL_NORM  DOUBLE,
   LASTMOD       TIMESTAMP,
   PAYVAL_DOC    DOUBLE,
   PAYVAL_HOME   DOUBLE,
   PAYVAL_ALT    DOUBLE,
   PAYVAL_NORM   DOUBLE
);

ALTER TABLE MASTER_DATA.AR_ITEM_CLOSED
   ADD PRIMARY KEY (CMPCODE, DOCTYPE, DOCNUM, CUSTID);

