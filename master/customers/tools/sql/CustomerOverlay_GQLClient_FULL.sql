SELECT 
    TRIM(CLIENT_TABLE.CLIENT_NUMBER) AS CUSTID, 
    TRIM(CLIENT_TABLE.CLIENT_NAME) AS NAME, 
    TRIM(CLIENT_TABLE.CLIENT_ADDRESS_LINE_1) AS ADD1, 
    TRIM(CLIENT_TABLE.CLIENT_ADDRESS_LINE_2) AS ADD2,
    TRIM(CLIENT_TABLE.CLIENT_ADDRESS_LINE_3) AS ADD3,
    TRIM(CLIENT_TABLE.CLIENT_CITY) AS CITY,
    TRIM(CLIENT_TABLE.CLIENT_PROVINCE) AS STATEPROV,
    TRIM(CLIENT_TABLE.CLIENT_COUNTRY) AS COUNTRY, 
    TRIM(CLIENT_TABLE.CLIENT_POSTAL_CODE) AS POSTCODE,
    CLIENT_TABLE.CLIENT_START_DATE AS CREATEDATE,
    CAST(CLIENT_TABLE.PAYMENT_TERMS AS INTEGER) AS TERMS,
    CLIENT_TABLE.CREDIT_LIM_PER_CLIENT AS CREDITLIM,
    REV_SUMMARY_TABLE.ROLL_12_MTH_REV AS TTMREV,
    TRIM(CLIENT_TABLE.SALESFORCE_SERVICE_ID) AS SFID,
    TRIM(CLIENT_TABLE.CORPORATE_CODE) AS CORPCODE,
    CLIENT_TABLE.CREDIT_LIMIT_DATE AS CRLIMDATE,
    NULL AS ACCTSTAT,
    NULL AS ACCTTYPE,
    NULL AS BONDTYPE,
    TRIM(CLIENT_TABLE.PREV_CO_NO) AS PREVCO,
    TRIM(CLIENT_TABLE.SIC_CODE) AS SIC,
    TRIM(CLIENT_TABLE.PRIMARY_CCST) AS CST,
    TRIM(CLIENT_TABLE.CDN_NAME) AS CSM,
    TRIM(CLIENT_TABLE.CDN_TITLE) AS MSD,
    TRIM(CLIENT_TABLE.US_NAME) AS SC,
    TRIM(CLIENT_TABLE.BOND_SIGN_STATUS) AS WAIVERTYPE,
    TRIM(CLIENT_TABLE.INV_CURRENCY) AS INVCURR,
    TRIM(CLIENT_TABLE.CREDIT_CODE) AS CREDITCODE,
    TRIM(CLIENT_TABLE.LANG_CD) AS LANGUAGE,
    TRIM(CLIENT_TABLE.PARENT_CODE) AS PARENT,
    NULL AS REMITTO,
    TRIM(CLIENT_TABLE.REMARKS_1) AS REMARKS1,
    TRIM(CLIENT_TABLE.REMARKS_2) AS REMARKS2
FROM
    CLIENT_TABLE, REV_SUMMARY_TABLE  
WHERE 
    CLIENT_TABLE.CLIENT_NUMBER = REV_SUMMARY_TABLE.CLIENT_NUMBER;